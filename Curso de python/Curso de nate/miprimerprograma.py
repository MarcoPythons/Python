# comando max()  <---- este comando es para buscar el numero mas alto dentro de los datos que se le estan dando

numero_max = max(1, 2, 3, 2, 33, 44, 5)

print(numero_max)

numero_1 = int(input('ingrese el primer número: '))
numero_2 = int(input('ingrese el segundo número: '))
numero_3 = int(input('ingrese el tercer número: '))

numero_max = max(numero_1, numero_2, numero_3)

numero_min = min(numero_1, numero_2, numero_3)

print("el numero maximo es {0} y el numero minimo es {1}".format(numero_max, numero_min))


